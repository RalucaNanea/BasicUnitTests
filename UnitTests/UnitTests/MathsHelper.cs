
namespace UnitTests
{
    public class MathsHelper
    {
        public int Add(int a, int b)
        {
            int x = a + b;
            return x;
        }

        public int Subtract(int a, int b)
        {
            int x = a - b;
            return x;
        }

        public int Devide(int a, int b)
        {
            return a / b;
        }
    }
}
